#include "filler.h"

void	ft_print_grid(t_data *data)
{
	int	i;

	if (data)
	{
		i = 0;
		while (i < data->gy)
			ft_putendl_fd(data->grid[i++], 2);
	}
	ft_putchar_fd('\n', 2);
}
