#include "libft.h"

int		ft_memcmp(const void *str1, const void *str2, size_t n)
{
	size_t				i;
	unsigned char		*src1;
	unsigned char		*src2;

	src1 = (unsigned char*)str1;
	src2 = (unsigned char*)str2;
	i = 0;
	while (i < n)
	{
		if (src1[i] != src2[i])
			return (src1[i] - src2[i]);
		i++;
	}
	return (0);
}
