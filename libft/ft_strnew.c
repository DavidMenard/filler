#include "libft.h"

char	*ft_strnew(size_t size)
{
	char *str;

	str = (char*)malloc(size + 1);
	if (!str)
		return (NULL);
	while (size + 1)
	{
		str[size--] = '\0';
	}
	return (str);
}
