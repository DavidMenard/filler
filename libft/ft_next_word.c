#include "libft.h"

char	*ft_next_word(char *s)
{
	if (!s)
		return (NULL);
	while (*s)
	{
		if (*s == ' ' || *s == '\t' || *s == '\v' || *s == '\n' ||
		*s == 'f' || *s == '\r')
			s++;
		else
			break ;
	}
	return (s);
}
